#unordered ,have no defined order can't be refered by index or key
#unchangeable ,add new items but can't change existing value
#duplicates are not allowed

setcar={"audi","bmw","MG hector"}

#print the setcar values
print(setcar)
#{'MG hector', 'audi', 'bmw'}

#len of set
print(len(setcar))
#3

#datatype of set 
print(type(setcar))
#<class 'set'>

#use set keyword with double bracket to create set
setcreate=set(({"hello","all"}))
print(setcreate)
#{'hello', 'all'}

